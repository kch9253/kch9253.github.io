
public class Box {
	private int width;
	private int pock;
	private int higth;
	
	
	public Box(int width, int pock, int higth) {
		super();
		this.width = width;
		this.pock = pock;
		this.higth = higth;
	}


	public int getWidth() {
		return width;
	}


	public void setWidth(int width) {
		this.width = 10;
	}


	public int getPock() {
		return pock;
	}


	public void setPock(int pock) {
		this.pock = 15;
	}


	public int getHigth() {
		return higth;
	}


	public void setHigth(int higth) {
		this.higth = 5;
	}
	
	
}
