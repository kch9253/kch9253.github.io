public class Re10per {
	public static void main(String[] args) {
		System.out.println(55 == 55); // =하나는 같다지만 ==는 같냐고 물어보는거임 / ture
		System.out.println(55 != 55); // (!)부정을뜻함 (!)뜻은 바르냐? / false
		System.out.println(55 > 55); // 왼쪽이 크냐 , 오른쪽이 크냐 물어보는 / false
		System.out.println(55 < 55); // false
		System.out.println(55 >= 55); //크거나 같다로 내림 /ture
		System.out.println(55 <= 55); //ture
	}
}